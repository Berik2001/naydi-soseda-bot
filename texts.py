def match_message(name: str, username: str | None) -> str:
    """Сообщение о взаимном мэтче."""
    contact = f"👉 @{username}" if username else "👉 (у пользователя нет username)"
    return (
        "🎉 У вас мэтч!\n"
        f"{name} хочет с тобой познакомиться\n"
        f"{contact}"
    )

